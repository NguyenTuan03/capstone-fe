// Helper functions
export const getSessionById = (id: number): Session | undefined => {
  return sessions.find((session) => session.id === id);
};

export const getSessionsByCourseId = (courseId: number): Session[] => {
  return sessions.filter((session) => session.course.id === courseId);
};

export const getSessionsByStatus = (status: SessionStatus): Session[] => {
  return sessions.filter((session) => session.status === status);
};

export const getCompletedSessions = (): Session[] => {
  return getSessionsByStatus(SessionStatus.COMPLETED);
};

export const getScheduledSessions = (): Session[] => {
  return getSessionsByStatus(SessionStatus.SCHEDULED);
};

export const getInProgressSessions = (): Session[] => {
  return getSessionsByStatus(SessionStatus.IN_PROGRESS);
};

export const getCancelledSessions = (): Session[] => {
  return getSessionsByStatus(SessionStatus.CANCELLED);
};
